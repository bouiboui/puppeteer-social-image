import baseTemplate from "./base-template";
import { buildUnsplashUrl } from ".";

const handlebars = require("handlebars");

const head = `
  <link href="https://fonts.googleapis.com/css?family=Lato:400,500,600,700,800,900&display=swap" rel="stylesheet">
`;

const imageTemplate = body =>
  handlebars.compile(`
<div class="Main">
  {{#if backgroundImageUrl}}
    <div class="Image" style="background-image: {{#if backgroundImageOverlay}}linear-gradient(180deg, rgba(0,0,0,0.2) 0%, rgba(0,0,0,0.6) 100%), {{/if}}url('{{backgroundImageUrl}}');" />
  {{/if}}

  <div class="Inner">
    ${body}
  </div>
  {{#if includeWatermark}}
  <div class="Watermark">
    {{#if watermarkUrl}}Generated by <span class="Watermark-logo">{{watermarkUrl}}</span>{{/if}}
    {{#if watermark}}{{watermark}}{{/if}}
  </div>{{/if}}
</div>
`);

const buildStyles = ({
  background = "black",
  backgroundImageUrl,
  color = "white",
  fontFamily = '"Lato", Arial',
  additionalStyles = ""
} = {}) => `
${additionalStyles}

.Image {
  width: 100%;
  height: 100%;
  background-size: cover;  
  background-repeat: no-repeat;
  background-origin: center;
}

.Main {
  background: ${backgroundImageUrl ? "transparent" : background};
  position: relative;
  width: 100%;
  height: 100%;
}

.Inner {
  background: ${backgroundImageUrl ? "transparent" : background};
  color: ${color};
  font-family: ${fontFamily};
  width: 100%;
  height: 100%;
}

.Watermark {
  color: white;
  font-family: ${fontFamily};
  font-size: 24px;
  position: absolute;
  opacity: 0.8;
  right: 8px;
  bottom: 8px;
}

.Watermark-logo {
  font-weight: 900;
}
`;

export default ({
  body,
  styles,
  size,
  templateParams: {
    gradient = true,
    imageUrl,
    unsplashId,
    unsplashKeywords,
    ...templateParams
  },
  testMode = false
}) => {
  let backgroundImageUrl;

  if (imageUrl) {
    backgroundImageUrl = imageUrl;
  } else if (unsplashId) {
    backgroundImageUrl = buildUnsplashUrl({ unsplashId, size });
  } else if (unsplashKeywords) {
    backgroundImageUrl = buildUnsplashUrl({ unsplashKeywords, size });
  }

  return baseTemplate({
    body: imageTemplate(body)({
      backgroundImageAnchor: "C",
      backgroundImageOverlay: gradient,
      backgroundImageUrl,
      body,
      includeWatermark:
        templateParams.watermarkUrl || templateParams.watermark || false,
      ...templateParams
    }),
    height: size.height,
    head: testMode ? "" : head,
    styles: buildStyles({
      backgroundImageUrl,
      additionalStyles: styles,
      ...templateParams
    }),
    width: size.width
  });
};
